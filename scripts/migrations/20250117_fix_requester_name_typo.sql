-- 문제: purchase_requests 테이블에서 "나유성" 직원의 이름이 "나우성"으로 잘못 입력됨
-- 원인: 2025년 7월 2일 발주 생성 시 수동 입력 오타 (5건)
-- 해결: 모든 "나우성" → "나유성"으로 일괄 수정

